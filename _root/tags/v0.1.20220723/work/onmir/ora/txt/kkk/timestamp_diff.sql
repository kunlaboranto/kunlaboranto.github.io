SELECT 
       EXTRACT(DAY    FROM TIME2-TIME1)*24*60*60
     + EXTRACT(HOUR   FROM TIME2-TIME1)*60*60
     + EXTRACT(MINUTE FROM TIME2-TIME1)*60
     + EXTRACT(SECOND FROM TIME2-TIME1)
     --+ ROUND(EXTRACT( SECOND FROM TIME2-TIME1 )*1000) ms
  FROM 
(
SELECT to_timestamp('2019-01-21 17:41:09 665','YYYY-MM-DD HH24:MI:SS FF3') TIME2
     , to_timestamp('2019-01-21 17:40:51 466','YYYY-MM-DD HH24:MI:SS FF3') TIME1
  FROM DUAL
)
;
